    if c == 'F':
        U[2][0], U[2][1], U[2][2], R[0][0], R[1][0], R[2][0], D[2][0], D[2][1], D[2][2], L[0][0], L[1][0], L[2][0] = \
        L[0][0], L[1][0], L[2][0], U[2][0], U[2][1], U[2][2], R[0][0], R[1][0], R[2][0], D[2][0], D[2][1], D[2][2]
    if c == 'B':
        U[0][0], U[0][1], U[0][2], L[0][2], L[1][2], L[2][2], D[0][0], D[0][1], D[0][2], R[0][2], R[1][2], R[2][2] = \
        R[0][2], R[1][2], R[2][2], U[0][0], U[0][1], U[0][2], L[0][2], L[1][2], L[2][2], D[0][0], D[0][1], D[0][2]
    if c == 'U':
        B[0][0], B[0][1], B[0][2], R[0][0], R[0][1], R[0][2], D[0][0], D[0][1], D[0][2], L[0][0], L[0][1], L[0][2] = \
        L[0][0], L[0][1], L[0][2], B[0][0], B[0][1], B[0][2], R[0][0], R[0][1], R[0][2], D[0][0], D[0][1], D[0][2]
    if c == 'D':
        B[2][0], B[2][1], B[2][2], L[2][0], L[2][1], L[2][2], D[2][0], D[2][1], D[2][2], R[2][0], R[2][1], R[2][2] = \
        R[2][0], R[2][1], R[2][2], B[2][0], B[2][1], B[2][2], L[2][0], L[2][1], L[2][2], D[2][0], D[2][1], D[2][2]



